#**********************************************VARIABLES AND DATATYPE***************************************************************


# VARIABLE: It is a short name of any value.
#                 or
#     It is like a container where we can store values.

#------------------------------------------------------------------------------

# TYPES OF VARIABLE :
# According to function, variable are of two types:

# 1. GLOBAL VARIABLE: a variable declared at the top level of a script or module, making it accessible throughout that module.

# 2. LOCAL VARIABLE:  Local variables are defined inside of a function, and they only exist inside that function.


#--------------------------------------------------------------------------------
# LIMITATIONS OF VARIABLES:

# 1. VARIABLE SHOULD START WITH ALPHABET OTHERWISE PYTHON WILL NOT ACCEPT AS VARIABLE.
# EX. X=2

# 2. VARIABLE IS CASE SENSTIVE.
# EX. X=2
#     x=3

# 3. VARIABLES CAN'T CONTAIN ANY SPECIAL CHARACTER.
# EX. R$M=45 ❌
#     RAM=45 ✅

# 4. VARIABLE CAN BE ALPHA-NUMBERIC i.e FIRST ALPHABET THEN NUMBER
# EX. A2=4

# 5. VARIABLE CAN CONTAIN (_) UNDERSCORE .
# EX. A_2="TANU"

